using namespace std;

void solve() {
	int n; cin >> n;
	priority_queue<int> Q;

	for (int i = 1; i <= n; i++) {
		int x; cin >> x;
		Q.push(x);
	}

	while (!Q.empty()) {
		int top = Q.top(); Q.pop();
		if (top > Q.size()) {
			cout << "NO\n";
			return;
		}
		
		vector<int> tmp;
		while (top--) {
			int cur = Q.top();
			Q.pop();
			cur--;
			tmp.push_back(cur);
		}

		for (auto x : tmp) Q.push(x);
	}
	cout << "YES\n";
}

int main() {
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t; 
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
	return 0;
}

