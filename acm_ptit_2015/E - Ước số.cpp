using namespace std;

long long uocs[1000005];
long long sumUocs[1000005];

long long sum[1000005];
long long cnt[1000005];

void prepare() {
	for (int i = 0; i <= 1000000; i++) {
		sum[i] = 0;
		cnt[i] = 0;
		uocs[i] = 0;
		sumUocs[i] = 0;
	}

	for (int uoc = 1; uoc <= 1000000; uoc++) {
		for (int i = uoc; i <= 1000000; i += uoc) {
			sumUocs[i] += uoc;
			uocs[i]++;
		}
	}

	for (int i = 1; i <= 1000000; i++) {
		sum[i] = sum[i - 1] + sumUocs[i];
		cnt[i] = cnt[i - 1] + uocs[i];
	}
}

void solve() {
	int l, r; cin >> l >> r;
	if (l == 0) l = 1;

	long long _cnt = cnt[r] - cnt[l - 1];
	long long _sum = sum[r] - sum[l - 1];

	cout << _cnt << " " << _sum << endl;
}

int main() {
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	prepare();
	int t; 
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
	return 0;
}

