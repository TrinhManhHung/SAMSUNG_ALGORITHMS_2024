using namespace std;

void solve() {
	vector<string> v(3); cin >> v[0] >> v[1] >> v[2];
	int a[3]; a[0] = 1; a[1] = 2; a[2] = 3;
	
	vector<char> s;
	for (int i = 0; i < 3; i++) {
		for (auto x : v[i]) {
			s.push_back(x);
		}
	}

	sort(s.begin(), s.end(), greater<char>());
	for (auto x : s) cout << x;
	cout << endl;
}

