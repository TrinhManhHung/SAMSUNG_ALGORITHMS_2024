#include<iostream>
#include<algorithm>
#include<vector>
#include<map>
#include<set>
#include<cstring>
#include<string>
#include<math.h>
#include<climits>

using namespace std;

int a[1005][1005];
int rowReds[1005], rowBlues[1005];
int colReds[1005], colBlues[1005];

void solve() {
	for (int i = 1; i <= 1000; i++) {
		rowReds[i] = 0;
		rowBlues[i] = 0;

		colReds[i] = 0;
		colBlues[i] = 0;
	}

	int n, m; cin >> n >> m;
	
	for (int i = 1; i <= n; i++) {
		for (int j = 1; j <= m; j++) {
			char x; cin >> x;
			a[i][j] = x - '0';
		}
	}
	
	if (n == 0 || m == 0) {
		cout << 0 << endl;
		return;
	}

	for (int i = 1; i <= n; i++) {
		int cntBlue = 0, cntRed = 0;

		for (int j = 1; j <= m; j++) {
			if (a[i][j] == 1) cntBlue++;
			if (a[i][j] == 2) cntRed++;
		}

		rowBlues[i] = cntBlue;
		rowReds[i] = cntRed;
	}

	for (int j = 1; j <= m; j++) {
		int cntBlue = 0, cntRed = 0;

		for (int i = 1; i <= n; i++) {
			if (a[i][j] == 1) cntBlue++;
			if (a[i][j] == 2) cntRed++;
		}

		colBlues[j] = cntBlue;
		colReds[j] = cntRed;
	}

	long long res = 0;
	for (int i = 1; i <= n; i++) {
		for (int j = 1; j <= m; j++) {
			if (a[i][j] == 1) {
				res += (rowReds[i] * colReds[j]);
			}
			if (a[i][j] == 2) {
				res += (rowBlues[i] * colBlues[j]);
			}
			/*if (res) {
				cout << i << " " << j << endl;
				cout << res << endl;
			}*/
		}
	}

	cout << res << endl;
}

int main() {
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t; 
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
	return 0;
}

