using namespace std;

void solve() {
	int n; cin >> n;
	int T; cin >> T;
	int a[55];
	for (int i = 1; i <= n; i++) cin >> a[i];
	int sum = 0;
	for (int i = 1; i <= n; i++) {
		sum += a[i];
		if (sum > T) {
			cout << i - 1 << endl;
			return;
		}
	}
}

