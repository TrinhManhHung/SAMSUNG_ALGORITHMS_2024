#include<iostream>
#include<algorithm>
#include<vector>
#include<map>
#include<set>
#include<cstring>
#include<string>
#include<math.h>
#include<climits>

using namespace std;

void solve() {
	int n; cin >> n;
	string s = "";
	for (int i = 1; i <= n; i++) {
		s += to_string(i);
	}

	int cnt = 0;
	while (s.size() != 1) {
		string tmp = "";
		for (int i = 0; i < s.size(); i++) {
			if ((i & 1) == (cnt & 1)) {
				tmp += s[i];
			}
		}

		s = tmp;
		cnt++;
	}

	cout << s << endl;

}

int main() {
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t; 
	t = 1;
	cin >> t;
	while (t--) {
		solve();
	}
	return 0;
}

