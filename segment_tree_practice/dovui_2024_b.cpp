struct Node{
    int cnt, cnt_1;

    Node(int _cnt, int _cnt_1){
        cnt = _cnt;
        cnt_1 = _cnt_1;
    }
    Node(){
        cnt = 0;
        cnt_1 = 0;
    }
};

struct SegmentTree{
    int n;
    vector<Node> tree;
    vi lazy;

    SegmentTree(int _n){
        n = _n;
        tree.assign(4*n + 5, Node(0, 0));
        lazy.assign(4*n + 5, 0);
    }
    
    void build(int id, int tl, int tr){
        tree[id].cnt   = tr - tl + 1;
        tree[id].cnt_1 = 0;
        if(tl == tr){
            return;
        }

        int mid = (tl + tr) >> 1;
        build(2*id, tl, mid);
        build(2*id + 1, mid + 1, tr);
    }

    void pushDown(int id, int tl, int tr) {
        if (lazy[id] % 2 == 1) {
            swap(tree[id].cnt, tree[id].cnt_1);
            if (tl != tr) { // không phải nút lá
                lazy[2*id] ^= 1;
                lazy[2*id+1] ^= 1;
            }
        }
        lazy[id] = 0;
    }

    void update(int id, int tl, int tr, int u, int v){
        pushDown(id, tl, tr);
        if(tr < u || tl > v) return;
        if(u <= tl && tr <= v){
            lazy[id]++;
            pushDown(id, tl, tr);
            return;
        }

        
        int mid = (tl + tr) >> 1;
        update(2*id, tl, mid, u, v);
        update(2*id + 1, mid + 1, tr, u, v);

        tree[id].cnt = tree[2*id].cnt + tree[2*id + 1].cnt;
        tree[id].cnt_1 = tree[2*id].cnt_1 + tree[2*id + 1].cnt_1;
    }

    int get(int id, int tl, int tr, int u, int v){
        pushDown(id, tl, tr);

        if(tr < u || tl > v) return 0;
        if(u <= tl && tr <= v){
            return tree[id].cnt_1;
        }

        int mid = (tl + tr) >> 1;
        int sum = get(2*id, tl, mid, u, v);
        int sum2 = get(2*id + 1, mid + 1, tr, u, v);

        return sum + sum2;
    }

};

void HuyenMay(){   
    int n, q; cin >> n >> q;

    SegmentTree segTree(n);
    segTree.build(1, 1, n);
    
    while(q--){
        int type, l, r; cin >> type >> l >> r;
        l++; r++;
        // SHOW2(l, r);
        if(type == 0){
            segTree.update(1, 1, n, l, r);
            
        } else{
            int res = segTree.get(1, 1, n, l, r);
            cout << res << endl;
        }
    }
}   


